"""Benchmark report generators."""

import csv
import io
import json
from typing import List

from app.benchmark.metrics import BenchmarkMetrics


def generate_benchmark_report_json(
    benchmark_run_id: str,
    profile: str,
    metrics: List[BenchmarkMetrics],
    saturation: dict,
    duration_ms: float = 0,
) -> str:
    from dataclasses import asdict
    report = {
        "benchmark_run_id": benchmark_run_id,
        "profile": profile,
        "duration_ms": round(duration_ms, 2),
        "model_metrics": [asdict(m) for m in metrics],
        "saturation": saturation,
    }
    return json.dumps(report, indent=2, default=str)


def generate_benchmark_report_md(
    benchmark_run_id: str,
    profile: str,
    metrics: List[BenchmarkMetrics],
    saturation: dict,
) -> str:
    lines = [
        "# DeepField Benchmark Report",
        "",
        f"- **Benchmark Run ID:** {benchmark_run_id}",
        f"- **Profile:** {profile}",
        "",
        "## Model Comparison",
        "",
        "| Model | Hardware | Requests | p95 (ms) | Tok/s | RPS | Error% | Stable |",
        "|-------|----------|----------|----------|-------|-----|--------|--------|",
    ]
    for m in metrics:
        stable = "YES" if m.stable else "NO"
        lines.append(
            f"| {m.model_name} | {m.hardware_lane} | {m.total_requests} | "
            f"{m.p95_latency_ms:.0f} | {m.tokens_per_second:.1f} | "
            f"{m.requests_per_second:.1f} | {m.error_rate*100:.1f}% | {stable} |"
        )
    if saturation:
        lines.extend(["", "## Saturation Points", ""])
        for model, sat in saturation.items():
            max_c = sat.get("max_stable_concurrency", "N/A")
            max_rps = sat.get("max_stable_rps", "N/A")
            lines.append(f"- **{model}**: max stable concurrency={max_c}, max RPS={max_rps}")
    lines.extend(["", "---", "*Generated by DeepField Benchmark*", ""])
    return "\n".join(lines)


def generate_saturation_csv(metrics_by_concurrency: List[BenchmarkMetrics]) -> str:
    output = io.StringIO()
    writer = csv.writer(output)
    writer.writerow([
        "model_name", "hardware_lane", "concurrency_level",
        "total_requests", "successful_requests", "failed_requests",
        "p50_latency_ms", "p95_latency_ms", "p99_latency_ms",
        "tokens_per_second", "requests_per_second", "error_rate", "stable",
    ])
    for m in metrics_by_concurrency:
        writer.writerow([
            m.model_name, m.hardware_lane, m.concurrency_level,
            m.total_requests, m.successful_requests, m.failed_requests,
            m.p50_latency_ms, m.p95_latency_ms, m.p99_latency_ms,
            m.tokens_per_second, m.requests_per_second, m.error_rate, m.stable,
        ])
    return output.getvalue()
